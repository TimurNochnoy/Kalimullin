#include <iostream>
#include <stdio.h>

int main() {
	double a = 0;
	int i = 0, f = 0;
	std::cout << "Введите натуральное число: ";
	if (std::cin >> a) {
		if ((a > 0) && (a - int(a) == 0)) {
			for (i=2; i <= a/2; i++) {
				if (int(a)%i == 0){
					f = 1;
				}
			}
			if (f == 0) {
				std::cout << "Это простое число!" << "\n";
			}
			else {
				std::cout << "Это составное число!" << "\n";
			}
		}
		else {
			std::cout << "Ошибка!" << "\n";
		}
	}
	else {
		std::cout << "Ошибка!" << "\n";
	};
	return 0;
}